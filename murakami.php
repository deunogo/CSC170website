            <h2>Overview</h2>

            <img class="scalabe" src="images/4.png" alt="Photo of Takashi Murakami">

            <p>Takashi Murakami (村上 隆, Murakami Takashi, born February 1, 1962) is a Japanese contemporary artist. He
                works in fine arts media (such as painting and sculpture) as well as commercial media (such as fashion,
                merchandise, and animation) and is known for blurring the line between high and low arts. He coined the
                term "superflat", which describes both the aesthetic characteristics of the Japanese artistic tradition
                and the nature of postwar Japanese culture and society, and is also used for Murakami's artistic style
                and other Japanese artists he has influenced. Murakami is also the founder and President of Kaikai Kiki
                Co., Ltd., through which he manages several youngee artists. He was the founder and organizer of the
                biannual art fair Geisa.</p>

            <h2>Life and career</h2>

            <h3>Academic background and early career</h3>

          

            <p>Murakami was born and raised in Tokyo, Japan. From early on, he was a fan of anime and manga (Japanese
                comics), and hoped to work in the animation industry. He attended Tokyo University of the Arts to
                acquire the drafting skills necessary to become an animator, but eventually majored in Nihonga, the
                'traditional' style of Japanese painting that incorporates traditional Japanese artistic conventions,
                techniques and subjects. He earned his master's degree in 1988. Though he would go on to earn a Ph.D. in
                Nihonga (1993), he gradually became disillusioned with its insular, highly political world and started
                to explre more contemporary artistic styles, media, and strategies.</p>

            <p>Murakami was dissatisfied with the state of contemporary art in Japan, believing it to be "a deep
                appropriation of Western trends." Thus, much of his early work was done in the spirit of social
                criticism and satire. On an article naming and explaining all of Murakami's pieces lies the infamous My
                Lonesome Cowboy, a companion to his earlier Hiropon. The sculpture is that of a naked anime character
                with blond spiky hair with a spiral trail of semen circling him. This piece is Murakami's most expensive
                piece to date selling for $13,500,000 at Sothbye's New York auction in 2008. Efforts from this period
                include performance art (Osaka Mixer Project, 1992), parodies of the "message" art popular in Japan in
                the early '90s, (Dobozite Dobozite Oshamanbe, 1993), and conceptual works (e.g. Randoseru Project,
                1991). He also began developing his own pop icon, "Mr. DOB," which would later develop into a form of
                self-portraiture, the first of several endlessly morphing and recurring motifs seen throughout his work.
                Though he garnered attention, many of his early pieces were not initially well received in Japan.</p>

                <h3>New York</h3>

                <p>In 1994, Murakami received a fellowship from the Asian Cultural Council and participated in the PS1
                    International Studio Program in New York City for a year. During his stay, he was exposed to and
                    highly inspired by Western contemporary artists such as Anselm Kiefer and especially the
                    simulationism of artists such as Jeff Koons. He established a small studio, which, together with the
                    Hiropon Factory in Japan, became the precursor to his company Kaikai Kiki. After returning to Japan,
                    he would develop the core concepts behind his artistic practice and begin exhibiting regularly at
                    major galleries and institutions across Europe and America..</p>
   

            <h2>Superflat</h2>

            <p>In 2000, Murakami published his "Superflat" theory in the catalogue for a group exhibition of the same
                name that he curated for the Museum of Contemporary Art, Los Angeles. The theory posits that there is a
                legacy of flat, 2-dimensional imagery from Japanese art history in manga and anime. This style
                differentiates itself from the western approach in its emphasis on surface and use of flat planes of
                color. Superflat also served as a commentary on postwar Japanese society in which, Murakami argues,
                differences in social class and popular taste have 'flattened,' producing a culture with little
                distinction between 'high' and 'low'. The theory provided the context for his work and he elaborated on
                it with the exhibitions "Coloriage" (2002, Fondation Cartier pour l'art contemporain, Paris) and "Little
                Boy: The Arts of Japan's Exploding Subculture" (2005, Japan Society, New York). These helped introduce
                Japan's lesser-known creative culture overseas and such curatorial projects would become an integral
                part of Murakami's multifaceted artistic practice.</p>

