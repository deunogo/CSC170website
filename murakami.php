            <h2>Collaborations</h2>

            <p>In 2002, at the invitation of designer Marc Jacobs, Murakami began his long-lasting collaboration with
                the fashion brand Louis Vuitton. He began by contributing artwork which was used in the design of a
                series of handbags. The series re-envisioned the company's monogram and was a huge commercial success.
                Though he had previously collaborated with fashion designers such as Issey Miyake Men by Naoki Takizawa,
                his work with Louis Vuitton made him widely known for blurring the line between 'high art' and
                commercialism. It also elevated him to celebrity status in his home country of Japan.</p>

            <p> In 2007, Murakami provided the cover artwork for rapper Kanye West's album Graduation and directed an
                animated music video for West's song "Good Morning". He also provided cover artwork for West's 2018
                collaboration album Kids See Ghosts with Kid Cudi. For "Graduation" and "Good Morning", Murakami would
                later 're-appropriate' these projects by incorporating their imagery into his paintings and sculptures,
                further blurring the boundaries between art and commercial branding and even questioning the existence
                of such a boundary.</p>

            <p>In 2009, music producer Pharrell Williams unveiled a collaborative sculpture with
                Murakami at Art Basel, which Williams stated "illustrates the metaphor of value."</p>

            <p>In 2018, Takashi Murakami collaborated with fashion designer Virgil Abloh on a series
                of artworks, bringing the fashion world to the art world but ultimately transcending both to create
                something more. Takashi and Virgil discuss their careers and their collaboration at length in their
                interview for Cultured Magazine's fall 2018 issue where they are featured on the cover.</p>


            <p>In March 2019, Billie Eilish released one of two official music videos for you should see me in a crown,
                one being directed and animated by Takashi Murakami. Murakami stated in a press release that the
                anime-style video, which was animated using motion capture technology, took eight months for him to
                create. The video opens with an animated version of Eilish, dressed in a neon-green shirt and shorts,
                eventually morphing into a spider-like monster that wreaks havoc on a miniature city. The video features
                the "Blohsh", Eilish's signature logo, as well as Murakami's flowers.</p>

            <p>In March 2020, J Balvin released his album Colores featuring album cover designs and artwork by Takashi
                Murakami. The Murakami-designed artwork was carried over to merchandise to celebrate the release of his
                album.In April 2020, Supreme released a Box Logo Tee featuring artwork from Murakami. All the proceeds
                went to HELP USA, in order to support youth and families facing homelessness during the COVID-19
                pandemic</p>

            <p>Asked about straddling the line between art and commercial products Murakami responded:</p>

            <p class="full-bleed quote ">“I don't think of it as straddling. I think of it as changing the line. What
                I've been talking about for years is how in Japan, that line is less defined. Both by the culture and by
                the post-War economic situation. Japanese people accept that art and commerce will be blended; and in
                fact, they are surprised by the rigid and pretentious Western hierarchy of "high art." In the West, it
                certainly is dangerous to blend the two because people will throw all sorts of stones. But that's
                okay—I'm ready with my hard hat.”</p>

            <h2>Art Style</h2>
            <p>Murakami's art encompasses a wide range of media and is generally described as superflat. It has been
                noted for its use of color, incorporation of motifs from Japanese traditional and popular culture,
                flat/glossy surfaces, and content that could be described at once as "cute", "psychedelic", or
                "satirical". Among his best known recurring motifs are smiling flowers, iconic characters, mushrooms,
                skulls, Buddhist iconography, and the sexual complexes of otaku culture. One of Murakami's most famous
                pieces known as 'Hiropon' brings to light his love for otaku culture. The sculpture that was created in
                2001 is said to show the "otaku culture and its strange, shocking sexuality in full force" and again
                like its counterpart 'The Lonesome Cowboy' semen rests floating around the female sculpture.</p>

            <p>In addition to large paintings such as 727 (permanent collection Museum of Modern Art, New York) and Tan
                Tan Bo Puking – a.k.a. Gero Tan, he has also produced sculptures, balloons, 'all-over' wallpaper
                installations, animated works, prints, posters, and assorted merchandise.</p>

