		<h2>Who is him?</h2>

			<img class="scalabe" src="images/2.png" alt="Photo of Wes Anserson">
			
			<p>Wesley Wales Anderson (born May 1, 1969) is an American filmmaker. His films are known for their <strong>symmetry, eccentricity, and distinctive visual and narrative styles</strong>, and he is regarded by some critics as a modern-day example of the auteur. Three of his films – The Royal Tenenbaums, Moonrise Kingdom, and The Grand Budapest Hotel – appeared in BBC Culture's 2016 poll of the greatest films since 2000.</p>

			<p>Anderson was nominated for the Academy Award for Best Original Screenplay for <em>The Royal Tenenbaums (2001)</em>, <em>Moonrise Kingdom (2012)</em>, and <em>The Grand Budapest Hotel (2014)</em>, as well as the Academy Award for Best Animated Feature for the stop-motion films <em>Fantastic Mr. Fox (2009)</em> and <em>Isle of Dogs (2018)</em>. With The Grand Budapest Hotel, he received his first<strong> Academy Award </strong>nominations for Best Director and Best Picture and won the <strong>Golden Globe Award </strong>for Best Motion Picture – Musical or Comedy and the <strong>BAFTA Award </strong>for Best Original Screenplay. He currently runs the production company American Empirical Pictures, which he founded in 1998. He won the Silver Bear for Best Director for Isle of Dogs in 2018.</p>

		<h2>Early Life</h2>

		<p>Wesley Wales Anderson was born on May 1, 1969, in Houston, Texas. He is the son of Texas Ann Anderson (née Burroughs), a realtor and archaeologist, and Melver Leonard Anderson, who worked in advertising and public relations. He is the second of three boys; his parents divorced when he was eight. His older brother, Mel, is a physician, and his younger brother, Eric Chase Anderson, is a writer and artist whose paintings and designs have appeared in several of Anderson's films, such as The Royal Tenenbaums. Anderson is of English, Swedish and Norwegian ancestry.</p>

		<p>He graduated from St. John's School in Houston in 1987, which he later used as a prominent location throughout Rushmore. As a child, Anderson made silent films on his father's Super 8 camera which starred his brothers and friends, although his first ambition was to be a writer. Anderson worked part-time as a cinema projectionist while attending the University of Texas at Austin, where he met future collaborator Owen Wilson. He graduated in 1990 with a degree in philosophy.</p>

		<h2>Film career</h2>

		<h3>1990s</h3>

			<p>Anderson's first film was <em>Bottle Rocket (1996)</em>, based on a short film of the same name that he made with Luke and Owen Wilson. It was a crime caper about a group of young Texans aspiring to achieve major heists. It was well-reviewed but performed poorly at the box office.</p>

			<p>His next film was <em>Rushmore (1998)</em>, a quirky comedy about a high school student's crush on an elementary school teacher starring Jason Schwartzman, Bill Murray, and Olivia Williams. It was a critical success. The film launched Murray's second act as a respected actor within independent cinema. Murray has since appeared in every Anderson film to date. At the 1999 Independent Spirit Awards, Anderson won the Best Director award and Murray won the Best Supporting Male award. Murray also earned a nomination for Golden Globe Award for Best Supporting Actor – Motion Picture. In 2000, filmmaker Martin Scorsese praised Bottle Rocket and Rushmore. Since its release, the film has gained cult status, and in 2016, the film was selected for preservation in the United States National Film Registry by the Library of Congress as being deemed "culturally, historically, or aesthetically significant."</p>

		<h3>2000s</h3>

			<p>Anderson's next comedy-drama, <em>The Royal Tenenbaums (2001)</em>, was released. The film focuses on a successful and artistic New York City family and its ostracized patriarch played by Gene Hackman. The film also starred Anjelica Huston as the ex-wife and Ben Stiller, Luke Wilson, and Gwyneth Paltrow as the children. The film was a box-office and a critical success. It represented his greatest financial success until the Moonrise Kingdom in 2012, earning more than $50 million in domestic box-office receipts. The Royal Tenenbaums was nominated for an Academy Award and ranked by an Empire poll as the 159th greatest film ever made.</p>

			<p>Anderson's next feature was The Life Aquatic with <em>Steve Zissou (2004)</em> about a Jacques Cousteau-Esque documentary filmmaker played by Bill Murray. The film also starred Owen Wilson, Cate Blanchett, Willem Dafoe, Jeff Goldblum, Anjelica Huston, and Michael Gambon. The film serves as a classic example of Anderson's style, but its critical reception was less favorable than his previous films, and its box office did not match the heights of The Royal Tenenbaums. In September 2006, Steely Dan's Walter Becker and Donald Fagen released a tongue-in-cheek "letter of intervention" for Anderson's artistic "malaise" following the disappointing commercial and critical reception of The Life Aquatic with Steve Zissou, proclaiming themselves to be fans of World Cinema and of Anderson in particular. They offered Anderson their soundtrack services for his The Darjeeling Limited, including lyrics for a title track.</p>

			<p><em>The Darjeeling Limited (2007)</em> was about three emotionally distant brothers traveling together on a train in India. It reflected the more dramatic tone of The Royal Tenenbaums but faced criticisms similar to The Life Aquatic with Steve Zissou. Anderson has acknowledged that he went to India to film the movie partly as a tribute to Indian filmmaker Satyajit Ray, whose "films have also inspired all my other movies in different ways" (the film is dedicated to him). The film starred Anderson staples Jason Schwartzman and Owen Wilson in addition to Adrien Brody, and the script was co-written by Anderson, Schwartzman, and Roman Coppola.</p>

			<p>Anderson has also made several notable short films. In addition to the original Bottle Rocket short, he made the <em>Paris-set Hotel Chevalier (2007)</em>, which was created as a prologue to The Darjeeling Limited and starred Jason Schwartzman alongside Natalie Portman, and the <em>Italy-set Castello Cavalcanti (2013)</em>, which was produced by Prada and starred Jason Schwartzman as an unsuccessful race-car driver. Additionally, he has directed a number of television commercials for companies such as Stella Artois and Prada, including an elaborate American Express ad, in which he starred as himself. In 2008, Anderson was hired to write the screenplay of the American adaptation of My Best Friend, a French film, for producer Brian Grazer; Anderson's first draft was titled "The Rosenthaler Suite."</p>

			<p>In 2009, Anderson's stop-motion-animated film adaptation based on the Roald Dahl book Fantastic Mr. Fox was released. The film featured George Clooney, Meryl Streep, Owen Wilson, Bill Murray, Willem Dafoe, Jason Schwartzman, Adrien Brody, and Michael Gambon. The film was highly praised among critics and nominated for the Academy Award for Best Animated Feature, although it barely made back its production.</p>
	
		<h3>2010s</h3>

			<p>In 2012, Anderson's film the Moonrise Kingdom was released, debuting at the Cannes Film Festival, where it competed in the competition for the Palme d'Or. The film is a coming-of-age comedy set in a fictional New England town about two children who run away together and are followed by their families, and their small-town community. The two children are played by newcomers Kara Hayward and Jared Gilman. The film also includes performances in its ensemble from Bill Murray, Edward Norton, Bruce Willis, Frances McDormand, and Tilda Swinton. The film was emblematic of Anderson's style and earned Anderson another Academy Award nomination for his screenplay. The film was also a financial success, earning $68.3 million at the box office against a budget of only $16 million.</p>

			<p>In 2014, Anderson's next film, The Grand Budapest Hotel, was released and starred Ralph Fiennes, Tony Revolori, Saoirse Ronan, Jeff Goldblum, Willem Dafoe, F. Murray Abraham, as well as several of his regular collaborators, including Bill Murray, Owen Wilson, and Jason Schwartzman. It is mostly set in the 1930s and follows the adventures of M. Gustave, the hotel's concierge, making "a marvelous mockery of history, turning its horrors into a series of graceful jokes and mischievous gestures," according to The New York Times. The film represented one of Anderson's greatest critical and commercial successes, grossing nearly $175 million worldwide and earning dozens of award nominations, including nine Oscar nominations with four wins for Best Production Design, Best Costume Design, Best Makeup, and Best Original Score. These nominations also included his first for Best Director.</p>

			<p>Anderson returned to stop-motion animation with Isle of Dogs. Production on the film started in the United Kingdom in October 2016, and it was released in select theaters on March 23, 2018, and wide on April 6, 2018. The film received Academy Award nominations for Best Animated Feature and Best Original Score.</p>
	
		<h3>2020s</h3>

			<p>In August 2018, it was reported that Anderson is working on his next film, set in post-war France, and was set to begin filming at Angoulême, beginning in November 2018. Named The French Dispatch, the film stars Benicio Del Toro, Bill Murray, Frances McDormand, Owen Wilson, Willem Dafoe, Adrien Brody, and Timothée Chalamet, and was previously scheduled to be released in July 2020, before being delayed due to the COVID-19 pandemic to October 2020, before being pulled from the schedule, rumored now to be released in 2021.</p>
	
