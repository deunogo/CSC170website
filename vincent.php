		<h2> Life </h2>

		<h3>Early years</h3>

			<p> Vincent Willem van Gogh was born on 30 March 1853 in Groot-Zundert, in the predominantly Catholic province of North Brabant in the Netherlands. He was the oldest surviving child of Theodorus van Gogh (1822–1885), a minister of the Dutch Reformed Church, and his wife Anna Cornelia Carbentus (1819–1907). Van Gogh was given the name of his grandfather and of a brother stillborn exactly a year before his birth. Vincent was a common name in the Van Gogh family: his grandfather, Vincent (1789–1874), who received a degree in theology at the University of Leiden in 1811, had six sons, three of whom became art dealers. This Vincent may have been named after his own great-uncle, a sculptor (1729–1802).</p>

