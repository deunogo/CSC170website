            <h2>Art</h2>
            <h3>SAMO graffiti</h3>
            <figure>
                <img src="jean-images/samo.jpg" alt="Picture of Basquiat’s SAMO graffiti">
                <figcaption>Fig.4 - SAMO graffiti</figcaption>
            </figure>
            <p>
                Basquiat began painting graffiti in the late 1970s, often socializing and working alongside other
                artists of the subculture in the Bronx and Harlem. Graffiti artists often focused on figurative
                jean-images (cartoonish pictures of animals, people and objects), as well as simple 'tags' - logos
                or names designed to be a trademark or calling card, which was were Basquiat also began. But
                Basquiat's graffiti quickly developed in a more abstract direction, with the "SAMO" tag origins
                quite mysterious and loaded with symbolism.
            </p>
            <p>
                This particular black spray paint tag on a wall is emblematic of the SAMO works that Basquiat and
                his collaborator Al Diaz made between 1976 and 1980. Quickly applied to public spaces in the street
                and subway, the SAMO pieces conveyed short, sharp and frequently anti-materialist messages to
                passersby. Usually seen as a sign of trespassing and vandalism, graffiti in the hands of Diaz and
                Basquiat became a tool of artistic "branding", and represents an important stage in the development
                of Basquiat's work.
            </p>
            <p>
                The concept of SAMO, or <em>"Same Old Shit"</em>, was developed during Basquiat's involvement with a
                drama project in New York, where he conceived a character that was devoted to selling a fake
                religion. Diaz and Basquiat applied the implicit critique embodied by this snake-oil-salesman figure
                to the commercial and corporate enterprises that they saw hawking goods in public spaces across
                their city. They initially began to spray paint the slogans that made up the works across subway
                trains as a way of "letting off steam" but, as Diaz remembers, they rapidly realized that it
                fulfilled an important role when they compared the work to more conventional graffiti tags. As Diaz
                says, "SAMO was like a refresher course because there was a statement being made".
            </p>
            <p>
                After years of collaboration, Diaz and Basquiat chose to signify the end of their joint venture with
                the three-word announcement "SAMO IS DEAD". Carried out episodically in various cites as a piece of
                ephemeral graffiti art, the phrase surfaced repeatedly on gritty buildings, particularly those
                throughout Lower Manhattan, where much of the artistic scene Basquiat was part of was located.
            </p>
            <h3>Flexible</h3>
            <p>
                Flexible features two of Basquiat's most famous motifs: the griot and the venerable crown. A thin
                and emaciated black figure stares out from the canvas towards the viewer, its arms creating a closed
                circuit in what may be a reference to spiritualized energy, a concept which appears in several works
                featuring the griot.
            </p>
            <p>
                Whilst it's lack of distinguishing characteristics might imply an 'Everyman', the specifically
                African ethnicity of the figure provides a clear reference to Basquiat's own identity and
                background. In its color palette and the particular rendering of the human figure through thin limbs
                and a large head, the influence of the shapes and forms of traditional West African art is apparent.
                Art historian and Basquiat collaborator Fred Hoffman writes that the image represents a tribal king,
                one whose "posture, with arms raised and interlocked above his head, conveys confidence and
                authority, attributes of his heroism. The work also reflects Basquiat's development as an artist and
                is a synthesis of his influences, with the diagrammatic rendering of the figure's lungs and abdomen
                reminiscent of the young Basquiat's fascination with anatomical sketches from Gray's Anatomy.
            </p>
            <p>
                Given that the griot is traditionally a kind of wandering philosopher, street performer, and social
                commentator, it is possible that Basquiat saw himself taking up this role within the New York art
                world, which nurtured his artistic success but also swiftly exploited it for material profit. The
                image is painted on wooden slats, which Basquiat asked his assistants to remove from a fence that
                protected the boundary of the studio where he was working in Los Angeles. By removing this barrier,
                Basquiat made the property open, and able to be traversed across freely, perhaps reflecting his
                empathy and personal experience of the limits of public space as a homeless person in New York.
            </p>
