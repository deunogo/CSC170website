            <h2>Biography</h2>

            <img class="scalabe" src="images/1.png" alt="Photo of Jean-Michel Basquiat">
            
            <p>
                <strong>Jean-Michel Basquiat</strong> (French: [ʒɑ̃ miʃɛl baskja]; December 22, 1960 – August 12,
                1988) was an American artist of Haitian and Puerto Rican descent. Basquiat first achieved fame as
                part of SAMO, a graffiti duo who wrote enigmatic epigrams in the cultural hotbed of the Lower East
                Side of Manhattan during the late 1970s, where rap, punk, and street art coalesced into early
                hip-hop music culture. By the early 1980s, his neo-expressionist paintings were being exhibited in
                galleries and museums internationally. At 21, Basquiat became the youngest artist to ever take part
                in documenta in Kassel. At 22, he was the youngest to exhibit at the Whitney Biennial in New York.
                The Whitney Museum of American Art held a retrospective of his art in 1992.
            </p>
            <p>
                Basquiat's art focused on dichotomies such as wealth versus poverty, integration versus segregation,
                and inner versus outer experience. He appropriated poetry, drawing, and painting, and married text
                and image, abstraction, figuration, and historical information mixed with contemporary critique.
                Basquiat used social commentary in his paintings as a tool for introspection and for identifying
                with his experiences in the black community of his time, as well as attacks on power structures and
                systems of racism. Basquiat's visual poetics were acutely political and direct in their criticism of
                colonialism and support for class struggle.
            </p>

            <h3>Criticism</h3>
            <p>
                In a review for The Telegraph, critic Hilton Kramer begins his first paragraph by stating that
                Basquiat had no idea what the word "quality" meant. <strong> WHICH HE DID!</strong> The
                criticisms which follow relentlessly label Basquiat as a <em>"talentless hustler"</em> and a
                "street-smart but otherwise invincibly ignorant" arguing that art dealers of the time were "as
                ignorant about art as Basquiat himself." In saying that Basquiat's work never rose above "that
                lowly artistic station" of graffiti "even when his paintings were fetching enormous prices,"
                Kramer argued that graffiti art "acquired a cult status in certain New York art circles." Kramer
                further opined that "As a result of the campaign waged by these art-world entrepreneurs on
                Basquiat's behalf—and their own, of course—there was never any doubt that the museums, the
                collectors and the media would fall into line" when talking about the marketing of Basquiat's
                name.
            </p>

            <p>
                Since his death at the age of 27 from a heroin overdose in 1988, his work has steadily increased in
                value. At a Sotheby's auction in May 2017, Untitled, a 1982 painting by Basquiat depicting a black
                skull with red and yellow rivulets, sold for $110.5 million, becoming one of the most expensive
                paintings ever purchased. It also set a new record high for an American artist at auction.
            </p>

            <h2>Legacy</h2>
            <Div>
                Basquiat's legacy has influenced literature, film, music, and fashion. In 2018, a public square in
                the 13th arrondissement of Paris was named Place Jean-Michel Basquiat in his memory. For the 2020-21
                NBA season, the Brooklyn Nets honored Basquiat with a basketball jersey inspired by his art.
            </Div>
            <h3>Fashion</h3>
            <p>
                In 2007, Basquiat was listed among GQ's 50 Most Stylish Men of the Past 50 Years. Basquiat often
                painted in expensive Armani suits and he walked the runway for the Comme des Garçons Spring/Summer
                1987 collection. To commemorate Basquiat's runway appearance, Comme des Garçons featured his prints
                in the brand's Fall/Winter 2018 collection. Valentino's Fall/Winter 2006 collection paid homage to
                Basquiat. Sean John created a capsule collection for the 30th anniversary of Basquiat's death in
                2018. Apparel and accessories companies that have featured Basquiat's work include Uniqlo, Urban
                Outfitters, Supreme, Herschel Supply Co., Alice + Olivia, Olympia Le-Tan, DAEM,and Coach New York.
                Footwear companies such as Dr. Martens, Reebok, and Vivobarefoot have also collaborated with
                Basquiat's estate.
            </p>
            <h3>Film</h3>
            <p>
                Basquiat starred in Downtown 81, a vérité movie written by Glenn O'Brien and shot by Edo Bertoglio
                in 1981, but not released until 2000. In 1996, eight years after the artist's death, a biographical
                film titled Basquiat was released, directed by Julian Schnabel, with actor Jeffrey Wright starring
                as Basquiat. David Bowie played the part of Andy Warhol. Schnabel was interviewed during the film's
                script development as a personal acquaintance of Basquiat. Schnabel then purchased the rights to the
                project, believing that he could make a better film.
            </p>
            <p>
                In 2006, the Equality Forum featured Jean-Michel Basquiat during LGBT history month. A 2009
                documentary film, Jean-Michel Basquiat: The Radiant Child, directed by Tamra Davis, was first
                screened as part of the 2010 Sundance Film Festival and was shown on the PBS series Independent Lens
                in 2011. Tamra Davis discussed her friendship with Basquiat in a Sotheby's video, "Basquiat: Through
                the Eyes of a Friend". In 2017, Sara Driver directed a documentary film, Boom for Real: The Late
                Teenage Years of Jean-Michel Basquiat, which had its world premiere at the 2017 Toronto
                International Film Festival. In 2018, PBS broadcast a 90-minute documentary about Basquiat as part
                of the American Masters series, entitled Basquiat: Rage to Riches.
            </p>
            <h3>Literature</h3>
            <p>
                In 1991, poet Kevin Young produced a book, To Repel Ghosts, a compendium of 117 poems relating to
                Basquiat's life, individual paintings, and social themes found in the artist's work. He published a
                "remix" of the book in 2005. In 1993, a children's book was released titled Life Doesn't Frighten
                Me, which combines a poem written by Maya Angelou with art made by Basquiat. In 2000, writer
                Jennifer Clement wrote the memoir Widow Basquiat: A Love Story, based on the narratives told to her
                by Basquiat's former girlfriend Suzanne Mallouk. The first American edition was released in 2014. In
                2005, poet M. K. Asante published the poem "SAMO", dedicated to Basquiat, in his book Beautiful. And
                Ugly Too. In 2016, the children's book Radiant Child: The Story of Young Artist Jean-Michel
                Basquiat, written and illustrated by Javaka Steptoe, was released in 2016. The biography, told from
                the perspective of a young prodigy, won the Caldecott Medal in 2017. In 2019, illustrator Paolo
                Parisi wrote the graphic novel Basquiat: A Graphic Novel, following Basquiat's journey from
                street-art legend SAMO to international art-scene darling, up until his death.
            </p>
            <h3>Music</h3>
            <p>
                Shortly after Basquiat's death, guitarist Vernon Reid of New York City funk metal band Living Colour
                wrote a song called "Desperate People", released on their album Vivid. The song primarily addresses
                the drug scene of New York at that time. Vernon states that Basquiat's death inspired him to write
                the song after receiving a phone call from Greg Tate informing Vernon of Basquiat's death.
            </p>
            <p>
                On August 12, 2014, Revelation 13:18 released the single <em>"Old School"</em> featuring Jean-Michel
                Basquiat, along with the self-titled album Revelation 13:18 x Basquiat. The release date of "Old
                School" coincided with the anniversary of Basquiat's death. The single received attention after
                American rapper and producer Jay-Z dressed up as Basquiat for Halloween the same year as the release
                giving Revelation a nod. In April 2020, New York rock band the Strokes released their sixth studio
                album, The New Abnormal. The cover art featured Basquiat’s 1981 painting Bird on Money.
            </p>

