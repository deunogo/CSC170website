            <h4>Heroes and Saints</h4>
            <p>
                A prominent theme in Basquiat's work is the portrayal of historically prominent black figures,
                who were identified by Basquiat as black heroes and saints. His early works often featured the
                iconographic depiction of crowns and halos to distinguish heroes and saints in his specially
                chosen pantheon. "Jean-Michel's crown has three peaks, for his three royal lineages: the poet,
                the musician, the great boxing champion. Jean measured his skill against all he deemed strong,
                without prejudice as to their taste or age," said his friend and artist Francesco Clemente.
                Reviewing Basquiat's show at the Bilbao Guggenheim, Art Daily noted that "Basquiat's crown is a
                changeable symbol: at times a halo and at others a crown of thorns, emphasizing the martyrdom
                that often goes hand in hand with sainthood. For Basquiat, these heroes and saints are warriors,
                occasionally rendered triumphant with arms raised in victory." Basquiat was particularly a fan
                of bebop and cited saxophonist Charlie Parker as a hero. He frequently referenced Parker and
                other jazz musicians in paintings such as Charles the First (1982) and Horn Players (1983), and
                King Zulu (1986).
            </p>

            <h3>Riding with Death</h3>
            <figure>
                <img src="jean-images/riding-with-death.jpg" alt="Picture of Basquiat’s Riding with Death Painting">
                <figcaption>Fig.6 - Riding with Death</figcaption>
            </figure>
            <p>
                Riding with Death is one of Basquiat's final paintings, and one which can easily be read as
                representing his inner turmoil and increasing conviction that the racist, classist, and corrupt
                nature of America in the 1980s was visible everywhere, including in the art world. Painted in the
                weeks before his death, the bleakness and sadness of the image and its title is only enhanced by the
                knowledge that the artist's life would come to an end far too soon shortly after its completion.
            </p>
            <p>
                Riding with Death is one of Basquiat's final paintings, and one which can easily be read as
                representing his inner turmoil and increasing conviction that the racist, classist, and corrupt
                nature of America in the 1980s was visible everywhere, including in the art world. Painted in the
                weeks before his death, the bleakness and sadness of the image and its title is only enhanced by the
                knowledge that the artist's life would come to an end far too soon shortly after its completion.
            </p>
            <p>
                The central figures, although simply framed, are loaded with symbolism. The pair suggest a nihilism
                or journey towards death that is made more poignant by Basquiat's dependence upon heroin and other
                drugs at the time of its painting. Although the African figure is riding the skeleton and could
                therefore be read as being in a position of dominance, the assertive positioning of the skeleton
                suggests instead that it is in control, perhaps dragging the rider to the far side of the frame.
                Coupled with the distinction in colour between the two (a white skeleton and Black rider), this
                couple could be read as a metaphor for the repression and destruction of African societies by
                colonial powers, as well as the inequalities that existed within 1980s America for people of color.
                This work is an excellent example of the complex meanings Basquiat was able to communicate and
                suggest through a complex visual language regularly referred to by critics as 'primitive' or
                'naïve'. As this poignant image shows, Basquiat's work was in fact highly sophisticated and far more
                technically accomplished than is often credited.
            </p>
